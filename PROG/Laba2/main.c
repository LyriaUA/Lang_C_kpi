#include <stdio.h>
#include <stdlib.h>
#include <math.h>
#define _USE_MATH_DEFINES
int main()
{

    float a = -(M_PI / 2);
    float b = M_PI/2;
    float n = 7;
    float x, f1, f2, f3, h;

    h = (b-a)/n;

    printf("-------------------------------------------------------------------------------------------------------------------\n");
    printf("|                                        Function of one variable                                                 |\n");
    printf("-------------------------------------------------------------------------------------------------------------------\n");
    printf("|      x       |     pow(M_E, 2x)    |    exp(x*log(2))+exp((x/2)*log(M_E))   |        1 + pow(M_E, cos(x))       |\n");
    printf("-------------------------------------------------------------------------------------------------------------------\n");

    for ( int i = 1; i <= n; i++ ) {
        x = i*h;

        f1 = pow(M_E, 2*x);

        f2 = exp(x*log(2))+exp((x/2)*log(M_E)) ;

        f3 = 1 + pow(M_E, cos(x));


        printf("| %-11.2f  |   %-15.2g   |   %-34.2f   |  %-32.2f |  \n", x, f1, f2, f3);
        printf("-------------------------------------------------------------------------------------------------------------------\n");

    }

    printf("|                                           Creat By Nazar Kulyk                                                  |\n");
    printf("-------------------------------------------------------------------------------------------------------------------\n");

    return 0;
}
